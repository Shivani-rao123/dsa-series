//to see whether the character is lower or upper case
#include<iostream>
using namespace std;
int main(){
    char ch;
    cout<<"Enter the character: ";
    cin>>ch;
    if(ch>='A' && ch<='Z'){
        cout<<"The character is in upper case";
    }
    else if(ch>='a' && ch<='z'){
        cout<<"The character is in lower case";
    }
    else{
        cout<<"The character is not an alphabet";
    }

}
